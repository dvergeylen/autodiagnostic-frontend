<h4>🎯 Points forts :</h4>
<ul class="strengths">
  <li># Inspirant, charismatique</li>
  <li># Ambassadeur, porte-voix</li>
  <li># Sens des responsabilités</li>
  <li># Aptitude à motiver les autres</li>
</ul>
<h4>📜 Description :</h4>
<p>
  On dit de toi que tu es inspirant et motivant ! Et oui, qui d'autre que toi pour guider les autres qui, assez spontanément, t'écoutent et te suivent. Ton dynamisme et ta motivation donnent les impulsions nécessaires pour que se forme autour de toi une équipe véritablement engagée dans les actions ou le projet que tu portes.
</p>
<p>
  Les autres te sollicitent tout naturellement pour prendre des responsabilités. Et quand on te confie une tâche, non seulement tu l'acceptes volontiers, mais tu l'accomplis avec zèle et de façon souvent très autonome. 
</p>
<p>
  Enfin, force est de constater que tu es un excellent ambassadeur ! Tu es donc sollicité lorsqu'il s'agit de présenter et défendre un projet devant tous types de publics. Bref, tu es aussi le porte-voix de l'équipe, rôle qui te plait particulièrement.
</p>

<h4>🚀 Un conseil :</h4>
<p>
  Ce talent inné que tu as pour guider et inspirer les autres ne doit pas t'empêcher d'être aussi à l'écoute des autres, les soutenir, « faire équipe » avec eux, cela te permettra de combiner les talents du leader et du coéquipier, deux profils souvent opposés, alors qu'ils sont en réalité très complémentaires !
</p>

