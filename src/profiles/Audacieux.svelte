<h4>🎯 Points forts :</h4>
<ul class="strengths">
  <li># Hautes ambitions</li>
  <li># Confiance en soi, optimisme</li>
  <li># Motivation à se fixer des objectifs</li>
  <li># Capacités à rebondir après un échec</li>
</ul>
<h4>📜 Description :</h4>
<p>
  Alors, toi, on peut dire que tu es quelqu'un « qui en veut » dans la vie ! Cela bouillonne dans ta tête entre les projets que tu veux réaliser, ces personnes que tu dois absolument rencontrer, sans oublier ces lieux que tu juges essentiels de découvrir, etc. Bref, tu sais ce qui te fait rêver, ce qui t'intéresse et tu sais où tu vas. Chaque objectif atteint fait place au suivant, celui qui reste à relever, puisque tu es en perpétuel mouvement, en action.
</p>
<p>
  L'échec ne te fait pas peur. Au contraire, tu t'en saisis pour mieux comprendre pourquoi les choses n'ont pas fonctionné comme tu l'avais imaginé, pour rebondir, faire autrement ; bref repartir sur le chemin trépidant de ce que tu veux encore accomplir. 
</p>
<p>
  Tu relèves souvent avec succès les défis que tu te lances parce que tu crois en toi, en tes capacités, tes habilités et tes connaissances, pour y parvenir. Avec une telle confiance en toi et un tel optimisme, tu fais bouger les lignes ; tu déplaces des montagnes ; bref tu accomplis des missions qui te profitent, certes, mais qui peuvent aussi profiter aux autres et à la société en général.
</p>
<h4>🚀 Un conseil :</h4>
<p>
  N'hésite pas à utiliser ta confiance en toi et ta soif d'avancer pour construire des projets porteurs de sens, de valeurs, pour un monde meilleur, cela te permettra d'associer les talents de l'audacieux et de l'idéaliste, une belle combinaison !
</p>

