<h4>🎯 Points forts :</h4>
<ul class="strengths">
  <li># Idéalisme, sens des valeurs</li>
  <li># Convictions fortes</li>
  <li># Engagement, voire militantisme</li>
  <li># Goût pour les échanges, le débat</li>
</ul>
<h4>📜 Description :</h4>
<p>
  S'il y a bien quelqu'un qui a des convictions, c'est toi ! Différentes causes dans ce monde en transition te sont chères et tu n'hésites pas à l'afficher, à le revendiquer même parfois lorsque ton engagement se mue en véritable militantisme. Il s'agit pour toi de ne pas rester les bras croisés, mais d'agir pour défendre ou mener des combats qui permettront un avenir meilleur, un avenir que tu juges souhaitable. Tu apprécies d'ailleurs les échanges qui permettent de débattre de ces changements provoqués par l'action humaine et donc des enjeux de durabilité. 
</p>

<p>
  Ton idéalisme guide également tes choix. Ainsi, si quelqu'un te sollicite pour prendre part à un projet, tu ne prends pas la proposition « à la légère » en l'acceptant d'emblée. Non, tu réfléchis ; tu pèses le « pour » et le « contre » par rapport à qui tu es, tes aspirations, tes valeurs. Parmi ces valeurs, l'intégrité, l'honnêteté, le courage et l'engagement sont particulièrement importantes. Tu n'accepteras en fin de compte que si tu estimes le projet aligné sur ta vision du monde et les causes que tu défends.
</p>

<h4>🚀 Un conseil :</h4>
<p>
  N'hésite pas à t'engager comme porte-voix, comme ambassadeur, de tes nobles combats, cela te permettra d'associer les talents de l'idéaliste et du leader, une belle combinaison !
</p>

